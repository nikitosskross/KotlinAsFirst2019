/**
 * Средняя
 *
 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    var result = 0
    var reverted = n
    for (i in 0 until digitNumber(n)) {
        result *= 10
        result += reverted % 10
        reverted /= 10
    }
    return result
}

/**
 * Средняя
 *
 * Проверить, является ли заданное число n палиндромом:
 * первая цифра равна последней, вторая -- предпоследней и так далее.
 * 15751 -- палиндром, 3653 -- нет.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun isPalindrome(n: Int): Boolean {
    if (digitNumber(n) == 1) return true
    val dn = digitNumber(n) / 2
    val r = 10.0.pow(dn.toDouble()).toInt()
    var firstPart = n / r
    val secondPart = revert(n % r)
    if (digitNumber(n) % 2 != 0) {
        firstPart /= 10
    }
    return firstPart == secondPart
}

/**
 * Средняя
 *
 * Для заданного числа n определить, содержит ли оно различающиеся цифры.
 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun hasDifferentDigits(n: Int): Boolean {
    val length = digitNumber(n)
    if (length == 1) return false
    var digits = n
    val first = digits % 10
    for (i in 0 until length) {
        if (digits % 10 != first) return true
        digits /= 10
    }
    return false
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из квадратов целых чисел:
 * 149162536496481100121144...
 * Например, 2-я цифра равна 4, 7-я 5, 12-я 6.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun squareSequenceDigit(n: Int): Int {
    var sequenceCount = 0
    var i = 0
    val result: Int
    while (true) {
        i++
        var k = i * i
        sequenceCount += digitNumber(k)
        if (sequenceCount >= n) {
            while (sequenceCount != n) {
                k /= 10
                sequenceCount--
            }
            result = k % 10
            break
        }
    }
    return result
}


